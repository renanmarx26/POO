public class Cachorro {

    String raca;
    int tamanho;
    String cor;
    Boolean tem_rabo;

    void late() {
        if (tem_rabo = true) {
            System.out.println("Eu sou o " + raca + " de tamanho" + tamanho + ", minha cor é" + cor + " e eu tenho rabo.");
        }
        else{
            System.out.println("Eu sou o " + raca + " de tamanho" + tamanho + ", minha cor é" + cor + " e eu não tenho rabo.");
        }
    }
}
